
package clases;
public class Celulares {
 private String idCelular;
 private String marca;
 private String modelo;
 private String precio;
 private String idTelefonia;

    public Celulares() {
    }

    public String getIdCelular() {
        return idCelular;
    }

    public void setIdCelular(String idCelular) {
        this.idCelular = idCelular;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }

    public String getIdTelefonia() {
        return idTelefonia;
    }

    public void setIdTelefonia(String idTelefonia) {
        this.idTelefonia = idTelefonia;
    }
   
}
